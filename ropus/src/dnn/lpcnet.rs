pub fn apply_window(x: &mut [f32; WINDOW_SIZE]) {
    for i in 0..OVERLAP_SIZE {
        x[i] *= HALF_WINDOW[i];
        x[WINDOW_SIZE - 1 - i] *= HALF_WINDOW[i];
    }
}

pub fn forward_transform(out: &mut [Cpx], input: &[f32]) {
    let mut x = [Cpx::default(); WINDOW_SIZE];
    for i in 0..WINDOW_SIZE {
        x[i] = Cpx {
            r: input[i],
            i: 0.0,
        };
    }
    let mut y = [Cpx::default(); WINDOW_SIZE];
    opus_fft_320(&x, &mut y);
    out[..FREQ_SIZE].copy_from_slice(&y[..FREQ_SIZE]);
}

fn inverse_transform(out: &mut [f32; WINDOW_SIZE], input: &[Cpx]) {
    let mut x = [Cpx::default(); WINDOW_SIZE];
    for i in 0..FREQ_SIZE {
        x[i] = input[i];
    }
    for i in FREQ_SIZE..WINDOW_SIZE {
        x[i] = Cpx {
            r: x[WINDOW_SIZE - i].r,
            i: -x[WINDOW_SIZE - i].i,
        };
    }
    let mut y = [Cpx::default(); WINDOW_SIZE];
    opus_fft_320(&x, &mut y);
    out[0] = WINDOW_SIZE as f32 * y[0].r;
    for i in 1..WINDOW_SIZE {
        out[i] = WINDOW_SIZE as f32 * y[WINDOW_SIZE - i].r;
    }
}

pub fn lpcn_compute_band_energy(band_e: &mut [f32; NB_BANDS], x: &[Cpx]) {
    let mut sum = [0.0f32; NB_BANDS];
    for i in 0..NB_BANDS - 1 {
        let band_size = (EBAND5MS[i + 1] - EBAND5MS[i]) * WINDOW_SIZE_5MS;
        for j in 0..band_size {
            let bin = EBAND5MS[i] * WINDOW_SIZE_5MS + j;
            let tmp = x[bin].r * x[bin].r + x[bin].i * x[bin].i;
            let frac = j as f32 / band_size as f32;
            sum[i] += (1.0 - frac) * tmp;
            sum[i + 1] += frac * tmp;
        }
    }
    sum[0] *= 2.0;
    sum[NB_BANDS - 1] *= 2.0;
    band_e.copy_from_slice(&sum);
}

fn compute_band_energy_inverse(band_e: &mut [f32; NB_BANDS], x: &[Cpx]) {
    let mut sum = [0.0f32; NB_BANDS];
    for i in 0..NB_BANDS - 1 {
        let band_size = (EBAND5MS[i + 1] - EBAND5MS[i]) * WINDOW_SIZE_5MS;
        for j in 0..band_size {
            let bin = EBAND5MS[i] * WINDOW_SIZE_5MS + j;
            let tmp = x[bin].r * x[bin].r + x[bin].i * x[bin].i;
            let tmp = 1.0 / (tmp + 1e-9);
            let frac = j as f32 / band_size as f32;
            sum[i] += (1.0 - frac) * tmp;
            sum[i + 1] += frac * tmp;
        }
    }
    sum[0] *= 2.0;
    sum[NB_BANDS - 1] *= 2.0;
    band_e.copy_from_slice(&sum);
}

pub fn dct(out: &mut [f32], input: &[f32]) {
    // C computes `sum * sqrt(2./NB_BANDS)` in double and truncates to f32
    // at the store — matching that requires keeping `scale` at f64 and
    // doing the final multiply in f64 too, then casting back to f32.
    // Using an f32 pre-cast loses a ULP on some NB_BANDS values and
    // leaks through to `features[0]` bit-exactness (root cause of
    // Stage 8.6 LPCNet feature drift).
    let scale_f64 = (2.0f64 / NB_BANDS as f64).sqrt();
    for i in 0..NB_BANDS {
        let mut sum = 0.0f32;
        for j in 0..NB_BANDS {
            sum += input[j] * DCT_TABLE[j * NB_BANDS + i];
        }
        out[i] = (sum as f64 * scale_f64) as f32;
    }
}

fn idct(out: &mut [f32], input: &[f32]) {
    let scale_f64 = (2.0f64 / NB_BANDS as f64).sqrt();
    for i in 0..NB_BANDS {
        let mut sum = 0.0f32;
        for j in 0..NB_BANDS {
            sum += input[j] * DCT_TABLE[i * NB_BANDS + j];
        }
        out[i] = (sum as f64 * scale_f64) as f32;
    }
}

fn interp_band_gain(g: &mut [f32], band_e: &[f32]) {
    for v in g[..FREQ_SIZE].iter_mut() {
        *v = 0.0;
    }
    for i in 0..NB_BANDS - 1 {
        let band_size = (EBAND5MS[i + 1] - EBAND5MS[i]) * WINDOW_SIZE_5MS;
        for j in 0..band_size {
            let frac = j as f32 / band_size as f32;
            g[EBAND5MS[i] * WINDOW_SIZE_5MS + j] = (1.0 - frac) * band_e[i] + frac * band_e[i + 1];
        }
    }
}

/// Levinson-Durbin recursion from autocorrelation. Float path (no Q-shifts).
fn lpcn_lpc(lpc: &mut [f32], rc: &mut [f32], ac: &[f32], p: usize) -> f32 {
    let mut error = ac[0];
    for v in lpc[..p].iter_mut() {
        *v = 0.0;
    }
    for v in rc[..p].iter_mut() {
        *v = 0.0;
    }
    if ac[0] != 0.0 {
        for i in 0..p {
            let mut rr = 0.0f32;
            for j in 0..i {
                rr += lpc[j] * ac[i - j];
            }
            rr += ac[i + 1];
            let r = -rr / error;
            rc[i] = r;
            lpc[i] = r;
            for j in 0..(i + 1) >> 1 {
                let tmp1 = lpc[j];
                let tmp2 = lpc[i - 1 - j];
                lpc[j] = tmp1 + r * tmp2;
                lpc[i - 1 - j] = tmp2 + r * tmp1;
            }
            error -= r * r * error;
            if error < 0.001 * ac[0] {
                break;
            }
        }
    }
    error
}

fn lpc_from_bands(lpc: &mut [f32], ex: &[f32]) -> f32 {
    let mut ac = [0.0f32; LPC_ORDER + 1];
    let mut rc = [0.0f32; LPC_ORDER];
    let mut xr = [0.0f32; FREQ_SIZE];
    let mut x_auto = [Cpx::default(); FREQ_SIZE];
    let mut x_auto_time = [0.0f32; WINDOW_SIZE];
    interp_band_gain(&mut xr, ex);
    xr[FREQ_SIZE - 1] = 0.0;
    for i in 0..FREQ_SIZE {
        x_auto[i] = Cpx { r: xr[i], i: 0.0 };
    }
    inverse_transform(&mut x_auto_time, &x_auto);
    for i in 0..LPC_ORDER + 1 {
        ac[i] = x_auto_time[i];
    }
    // -40 dB noise floor.
    ac[0] += ac[0] * 1e-4 + 320.0 / 12.0 / 38.0;
    // Lag windowing.
    for i in 1..LPC_ORDER + 1 {
        ac[i] *= 1.0 - 6e-5 * (i * i) as f32;
    }
    lpcn_lpc(lpc, &mut rc, &ac, LPC_ORDER)
}

pub fn lpc_weighting(lpc: &mut [f32], gamma: f32) {
    let mut gi = gamma;
    for i in 0..LPC_ORDER {
        lpc[i] *= gi;
        gi *= gamma;
    }
}

pub fn lpc_from_cepstrum(lpc: &mut [f32], cepstrum: &[f32]) -> f32 {
    let mut ex = [0.0f32; NB_BANDS];
    let mut tmp = [0.0f32; NB_BANDS];
    tmp[..NB_BANDS].copy_from_slice(&cepstrum[..NB_BANDS]);
    tmp[0] += 4.0;
    idct(&mut ex, &tmp);
    for i in 0..NB_BANDS {
        ex[i] = 10.0f32.powf(ex[i]) * COMPENSATION[i];
    }
    lpc_from_bands(lpc, &ex)
}

