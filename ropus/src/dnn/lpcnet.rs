#[derive(Clone, Copy, Debug, Default)]
pub struct Cpx {
    pub r: f32,
    pub i: f32,
}

#[inline(always)]
fn cmul(a: Cpx, b: Cpx) -> Cpx {
    Cpx {
        r: a.r * b.r - a.i * b.i,
        i: a.r * b.i + a.i * b.r,
    }
}

/// Complex multiply with conjugate of second operand: a * conj(b).
#[inline(always)]
fn cmulc(a: Cpx, b: Cpx) -> Cpx {
    Cpx {
        r: a.r * b.r + a.i * b.i,
        i: a.i * b.r - a.r * b.i,
    }
}

#[inline(always)]
fn cadd(a: Cpx, b: Cpx) -> Cpx {
    Cpx {
        r: a.r + b.r,
        i: a.i + b.i,
    }
}

#[inline(always)]
fn csub(a: Cpx, b: Cpx) -> Cpx {
    Cpx {
        r: a.r - b.r,
        i: a.i - b.i,
    }
}

