/// Radix-4 DIT butterfly.
fn kf_bfly4(fout: &mut [Cpx], fstride: usize, m: usize, n: usize, mm: usize) {
    let m2 = 2 * m;
    let m3 = 3 * m;
    for i in 0..n {
        let base = i * mm;
        let mut tw1i = 0usize;
        let mut tw2i = 0usize;
        let mut tw3i = 0usize;
        for j in 0..m {
            let s0 = cmul(fout[base + m + j], tw(tw1i));
            let s1 = cmul(fout[base + m2 + j], tw(tw2i));
            let s2 = cmul(fout[base + m3 + j], tw(tw3i));
            let f0 = fout[base + j];
            let s5 = csub(f0, s1);
            let f0p = cadd(f0, s1);
            let s3 = cadd(s0, s2);
            let s4 = csub(s0, s2);
            fout[base + m2 + j] = csub(f0p, s3);
            fout[base + j] = cadd(f0p, s3);
            fout[base + m + j] = Cpx {
                r: s5.r + s4.i,
                i: s5.i - s4.r,
            };
            fout[base + m3 + j] = Cpx {
                r: s5.r - s4.i,
                i: s5.i + s4.r,
            };
            tw1i += fstride;
            tw2i += fstride * 2;
            tw3i += fstride * 3;
        }
    }
}

/// Radix-5 DIT butterfly.
fn kf_bfly5(fout: &mut [Cpx], fstride: usize, m: usize, n: usize, mm: usize) {
    let ya = tw(fstride * m);
    let yb = tw(fstride * 2 * m);
    for i in 0..n {
        let f0 = i * mm;
        let f1 = f0 + m;
        let f2 = f0 + 2 * m;
        let f3 = f0 + 3 * m;
        let f4 = f0 + 4 * m;
        for u in 0..m {
            let s0 = fout[f0 + u];
            let s1 = cmul(fout[f1 + u], tw(u * fstride));
            let s2 = cmul(fout[f2 + u], tw(2 * u * fstride));
            let s3 = cmul(fout[f3 + u], tw(3 * u * fstride));
            let s4 = cmul(fout[f4 + u], tw(4 * u * fstride));
            let s7 = cadd(s1, s4);
            let s10 = csub(s1, s4);
            let s8 = cadd(s2, s3);
            let s9 = csub(s2, s3);
            fout[f0 + u].r = s0.r + s7.r + s8.r;
            fout[f0 + u].i = s0.i + s7.i + s8.i;
            let s5 = Cpx {
                r: s0.r + s7.r * ya.r + s8.r * yb.r,
                i: s0.i + s7.i * ya.r + s8.i * yb.r,
            };
            let s6 = Cpx {
                r: s10.i * ya.i + s9.i * yb.i,
                i: -(s10.r * ya.i) - s9.r * yb.i,
            };
            fout[f1 + u] = csub(s5, s6);
            fout[f4 + u] = cadd(s5, s6);
            let s11 = Cpx {
                r: s0.r + s7.r * yb.r + s8.r * ya.r,
                i: s0.i + s7.i * yb.r + s8.i * ya.r,
            };
            let s12 = Cpx {
                r: -(s10.i * yb.i) + s9.i * ya.i,
                i: s10.r * yb.i - s9.r * ya.i,
            };
            fout[f2 + u] = cadd(s11, s12);
            fout[f3 + u] = csub(s11, s12);
        }
    }
}

/// 320-point FFT. Input is scaled by 1/320, bit-reversed, then four butterfly stages.
/// Matches `opus_fft_c` + `opus_fft_impl` for the kfft state.
fn opus_fft_320(fin: &[Cpx; WINDOW_SIZE], fout: &mut [Cpx; WINDOW_SIZE]) {
    // Bit-reverse copy with 1/N scaling.
    for i in 0..WINDOW_SIZE {
        let dst = FFT_BITREV[i] as usize;
        fout[dst] = Cpx {
            r: fin[i].r * FFT_SCALE,
            i: fin[i].i * FFT_SCALE,
        };
    }
    // Stage 1: radix-4, fstride=80, m=1, N=80, mm=4
    kf_bfly4(fout, 80, 1, 80, 4);
    // Stage 2: radix-4, fstride=20, m=4, N=20, mm=16
    kf_bfly4(fout, 20, 4, 20, 16);
    // Stage 3: radix-4, fstride=5, m=16, N=5, mm=64
    kf_bfly4(fout, 5, 16, 5, 64);
    // Stage 4: radix-5, fstride=1, m=64, N=1, mm=1
    kf_bfly5(fout, 1, 64, 1, 1);
}

