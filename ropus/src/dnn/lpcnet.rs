/// KISS99 PRNG state. Suitable for simulations, not cryptographic use.
#[derive(Clone, Debug, Default)]
pub struct Kiss99Ctx {
    pub z: u32,
    pub w: u32,
    pub jsr: u32,
    pub jcong: u32,
}

impl Kiss99Ctx {
    pub fn srand(&mut self, data: &[u8]) {
        self.z = 362436069;
        self.w = 521288629;
        self.jsr = 123456789;
        self.jcong = 380116160;
        let ndata = data.len();
        let mut i: usize = 3;
        while i < ndata {
            self.z ^= data[i - 3] as u32;
            self.w ^= data[i - 2] as u32;
            self.jsr ^= data[i - 1] as u32;
            self.jcong ^= data[i] as u32;
            self.rand();
            i += 4;
        }
        if i - 3 < ndata {
            self.z ^= data[i - 3] as u32;
        }
        if i - 2 < ndata {
            self.w ^= data[i - 2] as u32;
        }
        if i - 1 < ndata {
            self.jsr ^= data[i - 1] as u32;
        }
        // Fix potential short cycles (Rose 2018).
        if self.z == 0 || self.z == 0x9068FFFF {
            self.z += 1;
        }
        if self.w == 0 || self.w == 0x464FFFFF {
            self.w += 1;
        }
        if self.jsr == 0 {
            self.jsr += 1;
        }
    }

    pub fn rand(&mut self) -> u32 {
        let znew = 36969u32
            .wrapping_mul(self.z & 0xFFFF)
            .wrapping_add(self.z >> 16);
        let wnew = 18000u32
            .wrapping_mul(self.w & 0xFFFF)
            .wrapping_add(self.w >> 16);
        let mwc = (znew << 16).wrapping_add(wnew);
        // Swapped 13/17 from original 1999 to match KISS11 single maximal cycle.
        let mut shr3 = self.jsr ^ (self.jsr << 13);
        shr3 ^= shr3 >> 17;
        shr3 ^= shr3 << 5;
        let cong = 69069u32.wrapping_mul(self.jcong).wrapping_add(1234567);
        self.z = znew;
        self.w = wnew;
        self.jsr = shr3;
        self.jcong = cong;
        (mwc ^ cong).wrapping_add(shr3)
    }
}

