impl LPCNetEncState {
    pub fn new() -> Self {
        let mut st = Self::default();
        st.pitchdnn.init();
        st
    }

    pub fn load_model(&mut self, data: &[u8]) -> i32 {
        self.pitchdnn.load_model(data)
    }

    fn frame_analysis(&mut self, x_out: &mut [Cpx], ex: &mut [f32; NB_BANDS], input: &[f32]) {
        let mut x = [0.0f32; WINDOW_SIZE];
        x[..OVERLAP_SIZE].copy_from_slice(&self.analysis_mem);
        x[OVERLAP_SIZE..].copy_from_slice(&input[..FRAME_SIZE]);
        self.analysis_mem
            .copy_from_slice(&input[FRAME_SIZE - OVERLAP_SIZE..FRAME_SIZE]);
        apply_window(&mut x);
        forward_transform(x_out, &x);
        lpcn_compute_band_energy(ex, x_out);
    }

    pub fn compute_frame_features(&mut self, input: &[f32]) {
        let lp_b: [f32; 2] = [-0.84946, 1.0];
        let lp_a: [f32; 2] = [-1.54220, 0.70781];

        let mut aligned_in = [0.0f32; FRAME_SIZE];
        aligned_in[..TRAINING_OFFSET]
            .copy_from_slice(&self.analysis_mem[OVERLAP_SIZE - TRAINING_OFFSET..OVERLAP_SIZE]);

        let mut x_cpx = vec![Cpx::default(); FREQ_SIZE];
        let mut ex = [0.0f32; NB_BANDS];
        self.frame_analysis(&mut x_cpx, &mut ex, input);

        // Instantaneous frequency features.
        self.if_features[0] = fmax(
            -1.0,
            fmin(
                1.0,
                (1.0 / 64.0) * (10.0 * celt_log10_f32(1e-15 + x_cpx[0].r * x_cpx[0].r) - 6.0),
            ),
        );
        for i in 1..PITCH_IF_MAX_FREQ {
            let prod = cmulc(x_cpx[i], self.prev_if[i]);
            let norm_1 = 1.0 / (1e-15 + prod.r * prod.r + prod.i * prod.i).sqrt();
            let prod = Cpx {
                r: prod.r * norm_1,
                i: prod.i * norm_1,
            };
            self.if_features[3 * i - 2] = prod.r;
            self.if_features[3 * i - 1] = prod.i;
            self.if_features[3 * i] = fmax(
                -1.0,
                fmin(
                    1.0,
                    (1.0 / 64.0)
                        * (10.0
                            * celt_log10_f32(
                                1e-15 + x_cpx[i].r * x_cpx[i].r + x_cpx[i].i * x_cpx[i].i,
                            )
                            - 6.0),
                ),
            );
        }
        self.prev_if[..PITCH_IF_MAX_FREQ].copy_from_slice(&x_cpx[..PITCH_IF_MAX_FREQ]);

        // Cepstral coefficients with spectral floor.
        //
        // Bit-exactness with C depends on how `celt_log10` is compiled:
        // without `FLOAT_APPROX` (harness default) it expands to
        //   celt_log10(x) = 0.3010299957f * celt_log2(x)
        //   celt_log2(x)  = (float)(1.442695040888963387 * log(x))
        // — natural log done in double, rescaled, rounded to float,
        // then scaled again by the f32 constant. Rust's `f32::log10`
        // maps to libm `log10f` (single precision), which diverges.
        // Emulate the two-stage C path so we match bit-for-bit.
        let mut ly = [0.0f32; NB_BANDS];
        let (mut log_max, mut follow) = (-2.0f32, -2.0f32);
        for i in 0..NB_BANDS {
            let arg = 1e-2f32 + ex[i];
            let log2_f32 = (1.442695040888963387_f64 * (arg as f64).ln()) as f32;
            ly[i] = 0.3010299957_f32 * log2_f32;
            ly[i] = fmax(log_max - 8.0, fmax(follow - 2.5, ly[i]));
            log_max = fmax(log_max, ly[i]);
            follow = fmax(follow - 2.5, ly[i]);
        }
        dct(&mut self.features, &ly);
        self.features[0] -= 4.0;
        lpc_from_cepstrum(&mut self.lpc, &self.features);
        for i in 0..LPC_ORDER {
            self.features[NB_BANDS + 2 + i] = self.lpc[i];
        }

        // Shift pitch buffers.
        self.exc_buf.copy_within(FRAME_SIZE..PITCH_BUF_SIZE, 0);
        self.lp_buf.copy_within(FRAME_SIZE..PITCH_BUF_SIZE, 0);

        aligned_in[TRAINING_OFFSET..FRAME_SIZE]
            .copy_from_slice(&input[..FRAME_SIZE - TRAINING_OFFSET]);

        // LPC residual via FIR filter.
        let mut x_fir = [0.0f32; FRAME_SIZE + LPC_ORDER];
        x_fir[..LPC_ORDER].copy_from_slice(&self.pitch_mem);
        x_fir[LPC_ORDER..LPC_ORDER + FRAME_SIZE].copy_from_slice(&aligned_in);
        self.pitch_mem
            .copy_from_slice(&aligned_in[FRAME_SIZE - LPC_ORDER..FRAME_SIZE]);
        celt_fir_f32(
            &x_fir,
            &self.lpc,
            &mut self.lp_buf[PITCH_MAX_PERIOD..],
            FRAME_SIZE,
            LPC_ORDER,
        );

        // Pitch pre-filter.
        for i in 0..FRAME_SIZE {
            self.exc_buf[PITCH_MAX_PERIOD + i] =
                self.lp_buf[PITCH_MAX_PERIOD + i] + 0.7 * self.pitch_filt;
            self.pitch_filt = self.lp_buf[PITCH_MAX_PERIOD + i];
        }

        // Biquad LP filter on lp_buf.
        let mut lp_out = [0.0f32; FRAME_SIZE];
        biquad(
            &mut lp_out,
            &mut self.lp_mem,
            &self.lp_buf[PITCH_MAX_PERIOD..PITCH_MAX_PERIOD + FRAME_SIZE],
            &lp_b,
            &lp_a,
            FRAME_SIZE,
        );
        self.lp_buf[PITCH_MAX_PERIOD..PITCH_MAX_PERIOD + FRAME_SIZE].copy_from_slice(&lp_out);

        // Cross-correlation and pitch detection.
        let xcorr_len = PITCH_MAX_PERIOD - PITCH_MIN_PERIOD;
        let mut xcorr = [0.0f32; PITCH_MAX_PERIOD];
        celt_pitch_xcorr_f32(
            &self.exc_buf[PITCH_MAX_PERIOD..],
            &self.exc_buf,
            &mut xcorr,
            FRAME_SIZE,
            xcorr_len,
        );
        let ener0 = celt_inner_prod_f32(
            &self.exc_buf[PITCH_MAX_PERIOD..],
            &self.exc_buf[PITCH_MAX_PERIOD..],
            FRAME_SIZE,
        );
        let mut ener1: f64 = celt_inner_prod_f32(&self.exc_buf, &self.exc_buf, FRAME_SIZE) as f64;
        let mut ener_norm = [0.0f32; PITCH_MAX_PERIOD - PITCH_MIN_PERIOD];
        for i in 0..xcorr_len {
            let ener: f32 = ((1.0f32 + ener0) as f64 + ener1) as f32;
            self.xcorr_features[i] = 2.0 * xcorr[i];
            ener_norm[i] = ener;
            ener1 += self.exc_buf[i + FRAME_SIZE] as f64 * self.exc_buf[i + FRAME_SIZE] as f64
                - self.exc_buf[i] as f64 * self.exc_buf[i] as f64;
        }
        for i in 0..xcorr_len {
            self.xcorr_features[i] /= ener_norm[i];
        }

        self.dnn_pitch = self
            .pitchdnn
            .compute(&self.if_features, &self.xcorr_features);
        let pitch = (0.5 + 256.0 / 2.0f32.powf((1.0 / 60.0) * ((self.dnn_pitch + 1.5) * 60.0)))
            .floor() as i32;
        let pitch = pitch
            .max(PITCH_MIN_PERIOD as i32)
            .min(PITCH_MAX_PERIOD as i32 - 1) as usize;

        // Pitch correlation.
        let xx = celt_inner_prod_f32(
            &self.lp_buf[PITCH_MAX_PERIOD..],
            &self.lp_buf[PITCH_MAX_PERIOD..],
            FRAME_SIZE,
        );
        let yy = celt_inner_prod_f32(
            &self.lp_buf[PITCH_MAX_PERIOD - pitch..],
            &self.lp_buf[PITCH_MAX_PERIOD - pitch..],
            FRAME_SIZE,
        );
        let xy = celt_inner_prod_f32(
            &self.lp_buf[PITCH_MAX_PERIOD..],
            &self.lp_buf[PITCH_MAX_PERIOD - pitch..],
            FRAME_SIZE,
        );
        let mut frame_corr = xy / (1.0 + xx * yy).sqrt();
        // Soft-plus normalization.
        frame_corr = (1.0 + (5.0 * frame_corr).exp()).ln() / (1.0 + 5.0f32.exp()).ln();
        self.features[NB_BANDS] = self.dnn_pitch;
        self.features[NB_BANDS + 1] = frame_corr - 0.5;
    }

    fn compute_single_frame_features_impl(
        &mut self,
        x: &mut [f32],
        features: &mut [f32; NB_TOTAL_FEATURES],
    ) -> i32 {
        #[allow(clippy::unnecessary_to_owned)] // x is both output and input; copy required
        preemphasis(
            x,
            &mut self.mem_preemph,
            &x.to_vec(),
            PREEMPHASIS_COEF,
            FRAME_SIZE,
        );
        self.compute_frame_features(x);
        features.copy_from_slice(&self.features);
        0
    }

    pub fn compute_single_frame_features(
        &mut self,
        pcm: &[i16],
        features: &mut [f32; NB_TOTAL_FEATURES],
    ) -> i32 {
        let mut x = [0.0f32; FRAME_SIZE];
        for i in 0..FRAME_SIZE {
            x[i] = pcm[i] as f32;
        }
        self.compute_single_frame_features_impl(&mut x, features)
    }

    pub fn compute_single_frame_features_float(
        &mut self,
        pcm: &[f32],
        features: &mut [f32; NB_TOTAL_FEATURES],
    ) -> i32 {
        let mut x = [0.0f32; FRAME_SIZE];
        x[..FRAME_SIZE].copy_from_slice(&pcm[..FRAME_SIZE]);
        self.compute_single_frame_features_impl(&mut x, features)
    }
}

