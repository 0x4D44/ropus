/// Embedding lookup table.
#[derive(Clone, Debug, Default)]
pub struct EmbeddingLayer {
    pub weights: Vec<f32>,
    pub dim: usize,
}

impl EmbeddingLayer {
    pub fn compute(&self, output: &mut [f32], idx: usize) {
        let start = idx * self.dim;
        if start + self.dim <= self.weights.len() {
            output[..self.dim].copy_from_slice(&self.weights[start..start + self.dim]);
        } else {
            output[..self.dim].fill(0.0);
        }
    }

    pub fn accum(&self, output: &mut [f32], idx: usize) {
        let start = idx * self.dim;
        if start + self.dim <= self.weights.len() {
            for i in 0..self.dim {
                output[i] += self.weights[start + i];
            }
        }
    }
}

