fn silk_energy_flp(data: &[f32], size: usize) -> f64 {
    let mut result = 0.0f64;
    let mut i = 0;
    while i < size.saturating_sub(3) {
        result += data[i] as f64 * data[i] as f64
            + data[i + 1] as f64 * data[i + 1] as f64
            + data[i + 2] as f64 * data[i + 2] as f64
            + data[i + 3] as f64 * data[i + 3] as f64;
        i += 4;
    }
    while i < size {
        result += data[i] as f64 * data[i] as f64;
        i += 1;
    }
    result
}

fn silk_inner_product_flp(d1: &[f32], d2: &[f32], size: usize) -> f64 {
    let mut result = 0.0f64;
    let mut i = 0;
    while i < size.saturating_sub(3) {
        result += d1[i] as f64 * d2[i] as f64
            + d1[i + 1] as f64 * d2[i + 1] as f64
            + d1[i + 2] as f64 * d2[i + 2] as f64
            + d1[i + 3] as f64 * d2[i + 3] as f64;
        i += 4;
    }
    while i < size {
        result += d1[i] as f64 * d2[i] as f64;
        i += 1;
    }
    result
}

pub fn silk_burg_analysis(
    a: &mut [f32],
    x: &[f32],
    min_inv_gain: f32,
    subfr_length: usize,
    nb_subfr: usize,
    d: usize,
) -> f32 {
    debug_assert!(subfr_length * nb_subfr <= 384);
    let c0_full = silk_energy_flp(x, nb_subfr * subfr_length);
    let mut c_first = [0.0f64; SILK_MAX_ORDER_LPC];
    for s in 0..nb_subfr {
        let xp = s * subfr_length;
        for n in 1..d + 1 {
            c_first[n - 1] += silk_inner_product_flp(&x[xp..], &x[xp + n..], subfr_length - n);
        }
    }
    let mut c_last = [0.0f64; SILK_MAX_ORDER_LPC];
    c_last[..d].copy_from_slice(&c_first[..d]);
    let mut caf = [0.0f64; SILK_MAX_ORDER_LPC + 1];
    let mut cab = [0.0f64; SILK_MAX_ORDER_LPC + 1];
    caf[0] = c0_full + FIND_LPC_COND_FAC * c0_full + 1e-9;
    cab[0] = caf[0];
    let mut inv_gain = 1.0f64;
    let mut reached_max = false;
    let mut af = [0.0f64; SILK_MAX_ORDER_LPC];
    for n in 0..d {
        for s in 0..nb_subfr {
            let xp = s * subfr_length;
            let (mut tmp1, mut tmp2) = (x[xp + n] as f64, x[xp + subfr_length - n - 1] as f64);
            for k in 0..n {
                c_first[k] -= x[xp + n] as f64 * x[xp + n - k - 1] as f64;
                c_last[k] -=
                    x[xp + subfr_length - n - 1] as f64 * x[xp + subfr_length - n + k] as f64;
                let atmp = af[k];
                tmp1 += x[xp + n - k - 1] as f64 * atmp;
                tmp2 += x[xp + subfr_length - n + k] as f64 * atmp;
            }
            for k in 0..=n {
                caf[k] -= tmp1 * x[xp + n - k] as f64;
                cab[k] -= tmp2 * x[xp + subfr_length - n + k - 1] as f64;
            }
        }
        let (mut tmp1, mut tmp2) = (c_first[n], c_last[n]);
        for k in 0..n {
            let atmp = af[k];
            tmp1 += c_last[n - k - 1] * atmp;
            tmp2 += c_first[n - k - 1] * atmp;
        }
        caf[n + 1] = tmp1;
        cab[n + 1] = tmp2;
        let mut num = cab[n + 1];
        let mut nrg_b = cab[0];
        let mut nrg_f = caf[0];
        for k in 0..n {
            let atmp = af[k];
            num += cab[n - k] * atmp;
            nrg_b += cab[k + 1] * atmp;
            nrg_f += caf[k + 1] * atmp;
        }
        let mut rc = -2.0 * num / (nrg_f + nrg_b);
        let tmp1_inv = inv_gain * (1.0 - rc * rc);
        if tmp1_inv <= min_inv_gain as f64 {
            rc = (1.0 - min_inv_gain as f64 / inv_gain).sqrt();
            if num > 0.0 {
                rc = -rc;
            }
            inv_gain = min_inv_gain as f64;
            reached_max = true;
        } else {
            inv_gain = tmp1_inv;
        }
        for k in 0..(n + 1) >> 1 {
            let (t1, t2) = (af[k], af[n - k - 1]);
            af[k] = t1 + rc * t2;
            af[n - k - 1] = t2 + rc * t1;
        }
        af[n] = rc;
        if reached_max {
            for k in n + 1..d {
                af[k] = 0.0;
            }
            break;
        }
        for k in 0..=n + 1 {
            let t1 = caf[k];
            caf[k] += rc * cab[n + 1 - k];
            cab[n + 1 - k] += rc * t1;
        }
    }
    let nrg_f;
    if reached_max {
        for k in 0..d {
            a[k] = -(af[k] as f32);
        }
        let mut c0 = c0_full;
        for s in 0..nb_subfr {
            c0 -= silk_energy_flp(&x[s * subfr_length..], d);
        }
        nrg_f = c0 * inv_gain;
    } else {
        let mut nf = caf[0];
        let mut t1 = 1.0f64;
        for k in 0..d {
            let atmp = af[k];
            nf += caf[k + 1] * atmp;
            t1 += atmp * atmp;
            a[k] = -(atmp as f32);
        }
        nrg_f = nf - FIND_LPC_COND_FAC * c0_full * t1;
    }
    if nrg_f < 0.0 { 0.0 } else { nrg_f as f32 }
}

fn compute_burg_cepstrum(pcm: &[f32], cepstrum: &mut [f32], len: usize, order: usize) {
    debug_assert!(order <= LPC_ORDER && len <= FRAME_SIZE);
    let mut burg_in = [0.0f32; FRAME_SIZE];
    for i in 0..len - 1 {
        burg_in[i] = pcm[i + 1] - PREEMPHASIS_COEF * pcm[i];
    }
    let mut burg_lpc = [0.0f32; LPC_ORDER];
    let g = silk_burg_analysis(&mut burg_lpc, &burg_in, 1e-3, len - 1, 1, order);
    let g = g / (len as f32 - 2.0 * (order as f32 - 1.0));
    let mut xbuf = [0.0f32; WINDOW_SIZE];
    xbuf[0] = 1.0;
    for i in 0..order {
        xbuf[i + 1] = -burg_lpc[i] * 0.995f32.powi(i as i32 + 1);
    }
    let mut lpc_spec = vec![Cpx::default(); FREQ_SIZE];
    forward_transform(&mut lpc_spec, &xbuf);
    let mut eburg = [0.0f32; NB_BANDS];
    compute_band_energy_inverse(&mut eburg, &lpc_spec);
    let ws3 = (WINDOW_SIZE as f32) * (WINDOW_SIZE as f32) * (WINDOW_SIZE as f32);
    for i in 0..NB_BANDS {
        eburg[i] *= 0.45 * g * (1.0 / ws3);
    }
    let mut ly = [0.0f32; NB_BANDS];
    let (mut log_max, mut follow) = (-2.0f32, -2.0f32);
    for i in 0..NB_BANDS {
        ly[i] = (1e-2 + eburg[i]).log10();
        ly[i] = fmax(log_max - 8.0, fmax(follow - 2.5, ly[i]));
        log_max = fmax(log_max, ly[i]);
        follow = fmax(follow - 2.5, ly[i]);
    }
    dct(cepstrum, &ly);
    cepstrum[0] -= 4.0;
}

pub fn burg_cepstral_analysis(ceps: &mut [f32], x: &[f32]) {
    let mut c0 = [0.0f32; NB_BANDS];
    let mut c1 = [0.0f32; NB_BANDS];
    compute_burg_cepstrum(x, &mut c0, FRAME_SIZE / 2, LPC_ORDER);
    compute_burg_cepstrum(&x[FRAME_SIZE / 2..], &mut c1, FRAME_SIZE / 2, LPC_ORDER);
    for i in 0..NB_BANDS {
        ceps[i] = 0.5 * (c0[i] + c1[i]);
        ceps[NB_BANDS + i] = c0[i] - c1[i];
    }
}

