#[derive(Clone, Debug)]
pub struct LPCNetPLCState {
    pub model: PLCModel,
    pub fargan: FARGANState,
    pub enc: LPCNetEncState,
    pub loaded: bool,
    pub arch: i32,
    // Reset-start marker: everything below cleared on reset.
    pub fec: Vec<[f32; NB_FEATURES]>,
    pub analysis_gap: bool,
    pub fec_read_pos: usize,
    pub fec_fill_pos: usize,
    pub fec_skip: usize,
    pub analysis_pos: usize,
    pub predict_pos: usize,
    pub pcm: Vec<f32>,
    pub blend: i32,
    pub features: [f32; NB_TOTAL_FEATURES],
    pub cont_features: Vec<f32>,
    pub loss_count: i32,
    pub plc_net: PLCNetState,
    pub plc_bak: [PLCNetState; 2],
}

impl Default for LPCNetPLCState {
    fn default() -> Self {
        Self {
            model: PLCModel::default(),
            fargan: FARGANState::default(),
            enc: LPCNetEncState::default(),
            loaded: false,
            arch: 0,
            fec: vec![[0.0; NB_FEATURES]; PLC_MAX_FEC],
            analysis_gap: true,
            fec_read_pos: 0,
            fec_fill_pos: 0,
            fec_skip: 0,
            analysis_pos: PLC_BUF_SIZE,
            predict_pos: PLC_BUF_SIZE,
            pcm: vec![0.0; PLC_BUF_SIZE],
            blend: 0,
            features: [0.0; NB_TOTAL_FEATURES],
            cont_features: vec![0.0; CONT_VECTORS * NB_FEATURES],
            loss_count: 0,
            plc_net: PLCNetState::default(),
            plc_bak: [PLCNetState::default(), PLCNetState::default()],
        }
    }
}

impl LPCNetPLCState {
    pub fn new() -> Self {
        let mut st = Self::default();
        st.init();
        st
    }

    pub fn init(&mut self) {
        self.fargan.init();
        self.enc = LPCNetEncState::new();
        self.loaded = false;
        self.reset();
    }

    /// Drop the per-stream runtime history, preserving loaded model
    /// weights and the `loaded` gate.
    ///
    /// Mirrors C `lpcnet_plc_reset` (`reference/dnn/lpcnet_plc.c:45`),
    /// which `OPUS_CLEAR`s from `LPCNET_PLC_RESET_START` onwards —
    /// the `fec` array. Fields *before* that marker in the C struct
    /// (`reference/dnn/lpcnet_private.h:50-71`) — `model`, `fargan`,
    /// `enc`, `loaded`, `arch` — survive the reset. C then explicitly
    /// re-initialises `enc` via `lpcnet_encoder_init` (which preserves
    /// PitchDNN weights but re-zeros encoder history buffers) and
    /// zero-fills `pcm` / `blend` / `loss_count` / `analysis_gap` /
    /// `analysis_pos` / `predict_pos` to their post-init values.
    ///
    /// Semantic consequence: a user-supplied `set_dnn_blob(custom)`
    /// followed by `reset()` keeps the custom weights live. The
    /// previous implementation wiped weights and re-applied embedded
    /// defaults, which silently reverted the user's override.
    pub fn reset(&mut self) {
        // Zero encoder runtime state while keeping its PitchDNN
        // weights. Rust doesn't have a `LPCNET_PLC_RESET_START`
        // marker, but `LPCNetEncState::new()` intentionally preserves
        // the `pitchdnn` field structure: the enclosing `PitchDNNState`
        // stays loaded because we keep `self.enc.pitchdnn` across this
        // call. We only overwrite the analysis/FFT buffers.
        let preserved_pitchdnn = std::mem::take(&mut self.enc.pitchdnn);
        self.enc = LPCNetEncState::new();
        self.enc.pitchdnn = preserved_pitchdnn;

        // `self.model`, `self.fargan`, `self.loaded`, `self.arch` are
        // intentionally NOT touched here — they sit before
        // `LPCNET_PLC_RESET_START` in the C layout.

        // Runtime history — everything from `fec` onwards in the C
        // struct.
        self.pcm.fill(0.0);
        self.blend = 0;
        self.loss_count = 0;
        self.analysis_gap = true;
        self.analysis_pos = PLC_BUF_SIZE;
        self.predict_pos = PLC_BUF_SIZE;
        self.fec_read_pos = 0;
        self.fec_fill_pos = 0;
        self.fec_skip = 0;
        self.features = [0.0; NB_TOTAL_FEATURES];
        self.cont_features.fill(0.0);
        for fec_row in self.fec.iter_mut() {
            fec_row.fill(0.0);
        }
        self.plc_net = PLCNetState::default();
        self.plc_bak = [PLCNetState::default(), PLCNetState::default()];
    }

    /// Parse the weight blob and populate every sub-model required for
    /// the neural PLC path.
    ///
    /// Mirrors C `lpcnet_plc_load_model` (`reference/dnn/lpcnet_plc.c:75`):
    ///
    /// 1. `init_plcmodel` — populates `self.model` (6 linear layers for
    ///    the PLC prediction GRUs and dense layers) from
    ///    `plcmodel_arrays`-style records.
    /// 2. `lpcnet_encoder_load_model` — delegates to
    ///    `pitchdnn_load_model` to populate the neural pitch estimator.
    /// 3. `fargan_load_model` — populates the FARGAN synthesis model.
    ///
    /// `self.loaded = true` is set **only** when all three succeed, so
    /// the SILK / CELT neural PLC path (which gates on `loaded`) runs
    /// with a fully populated state or not at all. A partial success
    /// leaves `loaded = false` and the caller falls through to the
    /// classical pitch/noise PLC on lost frames.
    ///
    /// Returns 0 on full success, non-zero on parse or population
    /// failure. The error code matches the step that failed, so a
    /// missing FARGAN record (for example) surfaces as -1 from
    /// `fargan.load_model`, not swallowed by an upstream step.
    ///
    /// Unknown / unused weight records are accepted silently (matches
    /// C semantics): the live tarball blob bundles DRED and OSCE
    /// weights we don't yet consume, and rejecting them would break
    /// the runtime path for no benefit.
    pub fn load_model(&mut self, data: &[u8]) -> i32 {
        // Parse the blob once so every sub-model sees the same arrays
        // without paying the parse cost three times. Keeps behaviour
        // identical to the C reference, which calls `parse_weights`
        // once in `lpcnet_plc_load_model`.
        let arrays = match parse_weights(data) {
            Ok(a) => a,
            Err(e) => {
                // Parse failure — we haven't mutated state yet, so the
                // previously-loaded weights stay live. Also clear the
                // gate: the caller's intent was to swap weights, and
                // leaving `loaded=true` on a failed swap would be a
                // lie about which weights are active.
                self.loaded = false;
                return e;
            }
        };

        // Atomic-load: build all three sub-models into locals first;
        // only commit to `self.*` after every build succeeds. This
        // prevents a "chimera" state where (e.g.) new PLC layers
        // coexist with stale FARGAN weights after a mid-load failure.
        let new_plc_model = match init_plcmodel(&arrays) {
            Ok(m) => m,
            Err(()) => {
                self.loaded = false;
                return -1;
            }
        };
        let new_pitchdnn_model = match PitchDNNState::try_init(&arrays) {
            Ok(m) => m,
            Err(()) => {
                self.loaded = false;
                return -1;
            }
        };
        let new_fargan_model = match FARGANState::try_init(&arrays) {
            Ok(m) => m,
            Err(()) => {
                self.loaded = false;
                return -1;
            }
        };

        // Commit point — past here, `self` is fully-populated with a
        // consistent set of weights. Nothing can fail between the
        // three commits (they are infallible moves).
        self.model = new_plc_model;
        self.enc.pitchdnn.apply_loaded_model(new_pitchdnn_model);
        self.fargan.apply_loaded_model(new_fargan_model);
        self.loaded = true;
        0
    }

    pub fn fec_add(&mut self, features: Option<&[f32]>) {
        match features {
            None => {
                self.fec_skip += 1;
            }
            Some(f) => {
                debug_assert!(self.fec_fill_pos < PLC_MAX_FEC);
                self.fec[self.fec_fill_pos][..NB_FEATURES].copy_from_slice(&f[..NB_FEATURES]);
                self.fec_fill_pos += 1;
            }
        }
    }

    pub fn fec_clear(&mut self) {
        self.fec_read_pos = 0;
        self.fec_fill_pos = 0;
        self.fec_skip = 0;
    }

    fn compute_plc_pred(&mut self, out: &mut [f32], input: &[f32]) {
        debug_assert!(self.loaded);
        let mut tmp = vec![0.0f32; self.model.plc_dense_in.nb_outputs.max(1)];
        compute_generic_dense(&self.model.plc_dense_in, &mut tmp, input, ACTIVATION_TANH);
        compute_generic_gru(
            &self.model.plc_gru1_input,
            &self.model.plc_gru1_recurrent,
            &mut self.plc_net.gru1_state,
            &tmp,
        );
        compute_generic_gru(
            &self.model.plc_gru2_input,
            &self.model.plc_gru2_recurrent,
            &mut self.plc_net.gru2_state,
            &self.plc_net.gru1_state.clone(),
        );
        compute_generic_dense(
            &self.model.plc_dense_out,
            out,
            &self.plc_net.gru2_state,
            ACTIVATION_LINEAR,
        );
    }

    fn get_fec_or_pred(&mut self, out: &mut [f32]) -> bool {
        if self.fec_read_pos != self.fec_fill_pos && self.fec_skip == 0 {
            let mut plc_features = vec![0.0f32; 2 * NB_BANDS + NB_FEATURES + 1];
            out[..NB_FEATURES].copy_from_slice(&self.fec[self.fec_read_pos]);
            self.fec_read_pos += 1;
            plc_features[2 * NB_BANDS..2 * NB_BANDS + NB_FEATURES]
                .copy_from_slice(&out[..NB_FEATURES]);
            plc_features[2 * NB_BANDS + NB_FEATURES] = -1.0;
            let mut discard = vec![0.0f32; NB_FEATURES];
            self.compute_plc_pred(&mut discard, &plc_features);
            true
        } else {
            let zeros = vec![0.0f32; 2 * NB_BANDS + NB_FEATURES + 1];
            self.compute_plc_pred(out, &zeros);
            if self.fec_skip > 0 {
                self.fec_skip -= 1;
            }
            false
        }
    }

    fn queue_features(&mut self, features: &[f32]) {
        self.cont_features
            .copy_within(NB_FEATURES..CONT_VECTORS * NB_FEATURES, 0);
        let start = (CONT_VECTORS - 1) * NB_FEATURES;
        self.cont_features[start..start + NB_FEATURES].copy_from_slice(&features[..NB_FEATURES]);
    }

    pub fn update(&mut self, pcm: &[i16]) -> i32 {
        if self.analysis_pos >= FRAME_SIZE {
            self.analysis_pos -= FRAME_SIZE;
        } else {
            self.analysis_gap = true;
        }
        if self.predict_pos >= FRAME_SIZE {
            self.predict_pos -= FRAME_SIZE;
        }
        self.pcm.copy_within(FRAME_SIZE..PLC_BUF_SIZE, 0);
        for i in 0..FRAME_SIZE {
            self.pcm[PLC_BUF_SIZE - FRAME_SIZE + i] = pcm[i] as f32 / 32768.0;
        }
        self.loss_count = 0;
        self.blend = 0;
        0
    }

    pub fn conceal(&mut self, pcm: &mut [i16]) -> i32 {
        debug_assert!(self.loaded);
        if self.blend == 0 {
            let mut count = 0;
            self.plc_net = self.plc_bak[0].clone();
            while self.analysis_pos + FRAME_SIZE <= PLC_BUF_SIZE {
                let mut x = [0.0f32; FRAME_SIZE];
                debug_assert!(self.analysis_pos < PLC_BUF_SIZE);
                for i in 0..FRAME_SIZE {
                    x[i] = 32768.0 * self.pcm[self.analysis_pos + i];
                }
                let mut plc_features = vec![0.0f32; 2 * NB_BANDS + NB_FEATURES + 1];
                burg_cepstral_analysis(&mut plc_features, &x);
                self.enc
                    .compute_single_frame_features_float(&x, &mut self.features);
                if (!self.analysis_gap || count > 0) && self.analysis_pos >= self.predict_pos {
                    self.queue_features(&self.features.clone());
                    plc_features[2 * NB_BANDS..2 * NB_BANDS + NB_FEATURES]
                        .copy_from_slice(&self.features[..NB_FEATURES]);
                    plc_features[2 * NB_BANDS + NB_FEATURES] = 1.0;
                    self.plc_bak[0] = self.plc_bak[1].clone();
                    self.plc_bak[1] = self.plc_net.clone();
                    let mut pred_out = vec![0.0f32; NB_FEATURES];
                    self.compute_plc_pred(&mut pred_out, &plc_features);
                    self.features[..NB_FEATURES].copy_from_slice(&pred_out);
                }
                self.analysis_pos += FRAME_SIZE;
                count += 1;
            }
            // Two prediction steps for lookahead.
            self.plc_bak[0] = self.plc_bak[1].clone();
            self.plc_bak[1] = self.plc_net.clone();
            let mut pred = vec![0.0f32; NB_FEATURES];
            self.get_fec_or_pred(&mut pred);
            self.features[..NB_FEATURES].copy_from_slice(&pred);
            self.queue_features(&self.features.clone());

            self.plc_bak[0] = self.plc_bak[1].clone();
            self.plc_bak[1] = self.plc_net.clone();
            let mut pred2 = vec![0.0f32; NB_FEATURES];
            self.get_fec_or_pred(&mut pred2);
            self.features[..NB_FEATURES].copy_from_slice(&pred2);
            self.queue_features(&self.features.clone());

            // Initialize FARGAN continuation.
            if PLC_BUF_SIZE >= FARGAN_CONT_SAMPLES {
                let start = PLC_BUF_SIZE - FARGAN_CONT_SAMPLES;
                let pcm_slice = self.pcm[start..PLC_BUF_SIZE].to_vec();
                let cont_feat = self.cont_features.clone();
                self.fargan.cont(&pcm_slice, &cont_feat);
            }
            self.analysis_gap = false;
        }

        // Common path for every lost frame.
        self.plc_bak[0] = self.plc_bak[1].clone();
        self.plc_bak[1] = self.plc_net.clone();
        let mut pred = vec![0.0f32; NB_FEATURES];
        let got_fec = self.get_fec_or_pred(&mut pred);
        self.features[..NB_FEATURES].copy_from_slice(&pred);
        if got_fec {
            self.loss_count = 0;
        } else {
            self.loss_count += 1;
        }

        // Energy attenuation.
        if self.loss_count >= 10 {
            self.features[0] = fmax(
                -15.0,
                self.features[0] + ATT_TABLE[9] - 2.0 * (self.loss_count - 9) as f32,
            );
        } else {
            self.features[0] = fmax(
                -15.0,
                self.features[0] + ATT_TABLE[self.loss_count as usize],
            );
        }

        let features_copy = self.features;
        self.fargan.synthesize_int(pcm, &features_copy);
        self.queue_features(&self.features.clone());

        if self.analysis_pos >= FRAME_SIZE {
            self.analysis_pos -= FRAME_SIZE;
        } else {
            self.analysis_gap = true;
        }
        self.predict_pos = PLC_BUF_SIZE;
        self.pcm.copy_within(FRAME_SIZE..PLC_BUF_SIZE, 0);
        for i in 0..FRAME_SIZE {
            self.pcm[PLC_BUF_SIZE - FRAME_SIZE + i] = pcm[i] as f32 / 32768.0;
        }
        self.blend = 1;
        0
    }
}

