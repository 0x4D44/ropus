#[inline]
fn celt_inner_prod_f32(x: &[f32], y: &[f32], n: usize) -> f32 {
    let mut sum = 0.0f32;
    for i in 0..n {
        sum += x[i] * y[i];
    }
    sum
}

fn celt_pitch_xcorr_f32(x: &[f32], y: &[f32], xcorr: &mut [f32], len: usize, max_pitch: usize) {
    for i in 0..max_pitch {
        xcorr[i] = celt_inner_prod_f32(x, &y[i..], len);
    }
}

/// FIR filter (float path). `x` points to the start of history; `y = x[0..n] + Σ num[j]*x[-(j+1)]`.
fn celt_fir_f32(x: &[f32], num: &[f32], y: &mut [f32], n: usize, ord: usize) {
    for i in 0..n {
        let mut sum = x[ord + i];
        for j in 0..ord {
            sum += num[j] * x[ord + i - j - 1];
        }
        y[i] = sum;
    }
}

pub fn preemphasis(y: &mut [f32], mem: &mut f32, x: &[f32], coef: f32, n: usize) {
    for i in 0..n {
        let yi = x[i] + *mem;
        *mem = -coef * x[i];
        y[i] = yi;
    }
}

fn biquad(y: &mut [f32], mem: &mut [f32; 2], x: &[f32], b: &[f32; 2], a: &[f32; 2], n: usize) {
    let (mut m0, mut m1) = (mem[0], mem[1]);
    for i in 0..n {
        let xi = x[i];
        let yi = xi + m0;
        let m0_old = m0;
        // Reordered to reduce dependency chains; +1e-30 forces ordering (no effect on output).
        m0 = (b[0] - a[0]) * xi + m1 - a[0] * m0;
        m1 = (b[1] - a[1]) * xi + 1e-30 - a[1] * m0_old;
        y[i] = yi;
    }
    mem[0] = m0;
    mem[1] = m1;
}

