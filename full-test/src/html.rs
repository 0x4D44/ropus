/// HTML-escape the five characters that can break out of element bodies or
/// attribute values. The set is deliberately conservative — apostrophe is
/// escaped too because attribute values sometimes use single quotes.
pub fn html_escape(s: &str) -> String {
    let mut out = String::with_capacity(s.len());
    for c in s.chars() {
        match c {
            '&' => out.push_str("&amp;"),
            '<' => out.push_str("&lt;"),
            '>' => out.push_str("&gt;"),
            '"' => out.push_str("&quot;"),
            '\'' => out.push_str("&#39;"),
            _ => out.push(c),
        }
    }
    out
}

/// Format a duration in milliseconds as human-readable `Xs` or `Xm Ys`.
pub fn format_duration(ms: u64) -> String {
    let total_secs = ms / 1000;
    if total_secs < 60 {
        return format!("{total_secs}s");
    }
    let m = total_secs / 60;
    let s = total_secs % 60;
    format!("{m}m {s}s")
}

/// One entry in the `<meta name="bench_ratios">` JSON array. `enc` / `dec` are
/// `None` when the ratio was missing (skipped fixture, crashed run, NaN/inf);
/// serde emits these as JSON `null` so the reader distinguishes "absent" from
/// "zero" without needing a sentinel value.
#[derive(Debug, Serialize, Deserialize, PartialEq)]
pub struct BenchRatio {
    pub label: String,
    pub enc: Option<f64>,
    pub dec: Option<f64>,
}

/// Serialise the current run's bench ratios as a JSON array that round-trips
/// through the `<meta name="bench_ratios">` tag so a future full-test run can
/// compute regression-vs-previous-report via `serde_json::from_str`. JSON
/// (rather than a homegrown `|`/`;` DSL) keeps label content lossless — a
/// label containing delimiter characters no longer collides.
fn bench_ratios_meta(vectors: &[VectorBench]) -> String {
    let rows: Vec<BenchRatio> = vectors
        .iter()
        .map(|v| BenchRatio {
            label: v.label.clone(),
            enc: v.enc_ratio.filter(|r| r.is_finite()),
            dec: v.dec_ratio.filter(|r| r.is_finite()),
        })
        .collect();
    // serde_json::to_string on a Vec<BenchRatio> cannot fail — the types are
    // all primitives/strings. Fall back to `[]` if somehow it does, rather
    // than poisoning the HTML.
    serde_json::to_string(&rows).unwrap_or_else(|_| "[]".to_string())
}

