fn render_header(out: &mut String, ctx: &ReportContext<'_>) {
    debug_assert_eq!(ctx.ietf_vectors_present, ctx.ietf_vectors.available());
    let ts = ctx.timestamp.format("%Y-%m-%d %H:%M:%S").to_string();
    out.push_str(&format!(
        "<h1>ropus Test Report <span class=\"badge\" style=\"background:{color}\">{label}</span></h1>\n",
        color = ctx.banner.badge_color(),
        label = html_escape(ctx.banner.label()),
    ));
    out.push_str(&format!(
        "<p class=\"meta\">Generated: {ts} &bull; Version: {ver} &bull; Branch: {br} &bull; Commit: {sha} &mdash; {subj}</p>\n",
        ts = html_escape(&ts),
        ver = html_escape(ctx.version),
        br = html_escape(ctx.branch),
        sha = html_escape(ctx.commit_sha),
        subj = html_escape(ctx.commit_subject),
    ));

    // Note strip — IETF vectors + flag surface.
    let mut notes: Vec<String> = Vec::new();
    notes.push(ietf_vectors_note(&ctx.ietf_vectors));
    if ctx.options.quick {
        notes.push("Flag: <code>--quick</code> (quality + benchmarks skipped).".to_string());
    }
    if ctx.options.skip_quality {
        notes.push("Flag: <code>--skip-quality</code>.".to_string());
    }
    if ctx.options.skip_coverage {
        notes.push("Flag: <code>--skip-coverage</code>.".to_string());
    }
    if ctx.options.skip_benchmarks {
        notes.push("Flag: <code>--skip-benchmarks</code>.".to_string());
    }
    if ctx.options.skip_ambisonics {
        notes.push("Flag: <code>--skip-ambisonics</code>.".to_string());
    }
    if ctx.options.release_preflight {
        notes.push("Flag: <code>--release-preflight</code> (core profile).".to_string());
    }
    if !notes.is_empty() {
        out.push_str("<p class=\"meta\">");
        for (i, n) in notes.iter().enumerate() {
            if i > 0 {
                out.push_str(" &bull; ");
            }
            // Notes contain trusted literal HTML (`<code>…</code>`) above;
            // dynamic parts are escaped before they enter the note string.
            out.push_str(n);
        }
        out.push_str("</p>\n");
    }
}

fn render_release_preflight(out: &mut String, preflight: &PreflightOutcome) {
    out.push_str("<h2>Release preflight</h2>\n");
    let mode = if preflight.release_preflight {
        "active"
    } else {
        "report-only"
    };
    out.push_str(&format!(
        "<p class=\"meta\">Profile: <code>{}</code> &bull; Mode: {}</p>\n",
        html_escape(preflight.profile),
        html_escape(mode),
    ));
    out.push_str("<table>\n<tr><th>Asset</th><th>Key</th><th>Requirement</th><th>Status</th><th>Probe</th><th>Note</th></tr>\n");
    for asset in &preflight.assets {
        let cls = preflight_status_class(asset.status, asset.banner_blocking);
        let probes = if asset.probes.is_empty() {
            "derived".to_string()
        } else {
            asset
                .probes
                .iter()
                .map(|probe| html_escape(probe))
                .collect::<Vec<_>>()
                .join("<br>")
        };
        let note = asset.note.as_deref().unwrap_or("");
        out.push_str(&format!(
            "<tr><td>{}</td><td><code>{}</code></td><td>{}</td><td class=\"{}\">{}</td><td>{}</td><td>{}</td></tr>\n",
            html_escape(asset.label),
            html_escape(asset.key),
            html_escape(asset.requirement.as_str()),
            cls,
            html_escape(asset.status.as_str()),
            probes,
            html_escape(note),
        ));
    }
    out.push_str("</table>\n");
}

fn preflight_status_class(status: AssetStatus, banner_blocking: bool) -> &'static str {
    if banner_blocking {
        return "fail";
    }
    match status {
        AssetStatus::MissingRequired => "fail",
        AssetStatus::MissingOptional => "warn",
        AssetStatus::PresentRequired
        | AssetStatus::ProvisionedRequired
        | AssetStatus::PresentOptional => "pass",
    }
}

fn ietf_vectors_note(v: &IetfVectorProvision) -> String {
    match v.status {
        ProvisionStatus::Present => "IETF vectors: present.".to_string(),
        ProvisionStatus::Provisioned => {
            let script = script_note(v);
            format!("IETF vectors: provisioned during this run{script}.")
        }
        ProvisionStatus::Unavailable => {
            let script = script_note(v);
            let reason = v
                .reason
                .as_deref()
                .map(html_escape)
                .unwrap_or_else(|| "no diagnostic available".to_string());
            format!(
                "<span class=\"fail\">IETF vectors unavailable after provisioning</span>{script}: {reason}"
            )
        }
    }
}

fn script_note(v: &IetfVectorProvision) -> String {
    match &v.script {
        Some(path) => format!(" via <code>{}</code>", html_escape(&path.to_string_lossy())),
        None => String::new(),
    }
}

/// The conformance integration-test binary names. Each file under
/// `tests/conformance/tests/*.rs` compiles to its own binary, and cargo's
/// `test <name>` lines carry the file stem as the binary label — not the
/// crate name `conformance`. Confirmed against the directory listing
/// 2026-04-19: `{api, decode, encode, extensions, ietf_vectors, padding,
/// projection}`. Update alongside changes to `tests/conformance/tests/`.
const CONFORMANCE_BINARIES: &[&str] = &[
    "api",
    "decode",
    "encode",
    "extensions",
    "ietf_vectors",
    "padding",
    "projection",
];

/// Category summary — roll up `per_test` FQNs into top-level buckets.
/// Buckets match HLD § Output format: CELT / SILK / Opus / DNN / Types /
/// Coverage / Conformance, plus an `Other` row (rendered only when non-zero)
/// so no test is silently swallowed — every FQN either lands in a known
/// bucket or shows up as `Other`. The "Coverage" bucket here refers to
/// `coverage_tests::*` (extra coverage-focused tests), not the llvm-cov
/// metrics block — distinct sections.
fn render_category_summary(out: &mut String, per_test: &[TestOutcome]) {
    out.push_str("<h2>Category summary</h2>\n");
    if per_test.is_empty() {
        out.push_str("<p class=\"empty\">No test results (stage skipped or build failed).</p>\n");
        return;
    }
    let mut buckets: BTreeMap<&'static str, (u32, u32, u32)> = BTreeMap::new();
    for name in [
        "CELT",
        "SILK",
        "Opus",
        "DNN",
        "Types",
        "Coverage",
        "Conformance",
        "Other",
    ] {
        buckets.insert(name, (0, 0, 0));
    }
    for t in per_test {
        let cat = categorise_fqn(&t.fqn, &t.binary);
        if let Some(slot) = buckets.get_mut(cat) {
            slot.0 += 1;
            match t.outcome {
                TestOutcomeKind::Pass => slot.1 += 1,
                TestOutcomeKind::Fail => slot.2 += 1,
                TestOutcomeKind::Ignored => {}
            }
        }
    }
    out.push_str("<table>\n<tr><th>Category</th><th class=\"num\">Total</th><th class=\"num\">Passed</th><th class=\"num\">Failed</th></tr>\n");
    // Stable render order matches the HLD list (not alphabetical). `Other`
    // is appended last and suppressed when empty — no zero-count row for
    // the expected case.
    for cat in [
        "CELT",
        "SILK",
        "Opus",
        "DNN",
        "Types",
        "Coverage",
        "Conformance",
    ] {
        let (total, passed, failed) = buckets.get(cat).copied().unwrap_or((0, 0, 0));
        // Emit as a single combined class so the result is valid HTML
        // (browsers tolerate `class="a" class="b"` but HTML5 disallows it).
        let fail_cls = if failed > 0 { " fail" } else { "" };
        out.push_str(&format!(
            "<tr><td>{}</td><td class=\"num\">{}</td><td class=\"num\">{}</td><td class=\"num{}\">{}</td></tr>\n",
            html_escape(cat), total, passed, fail_cls, failed,
        ));
    }
    let (other_total, other_passed, other_failed) =
        buckets.get("Other").copied().unwrap_or((0, 0, 0));
    if other_total > 0 {
        let fail_cls = if other_failed > 0 { " fail" } else { "" };
        out.push_str(&format!(
            "<tr><td>Other</td><td class=\"num\">{}</td><td class=\"num\">{}</td><td class=\"num{}\">{}</td></tr>\n",
            other_total, other_passed, fail_cls, other_failed,
        ));
    }
    out.push_str("</table>\n");
}

/// Classify an FQN into one of the category buckets. The logic is:
///
/// 1. Tests whose binary is one of the known conformance integration-test
///    binaries (see `CONFORMANCE_BINARIES`) or whose FQN contains
///    `conformance::` land in the Conformance bucket.
/// 2. Otherwise look at the FQN head — `silk::`, `celt::`, `opus::`, `dnn::`,
///    `types::`.
/// 3. Tests starting `coverage_tests::` feed the "Coverage" bucket (these are
///    the deliberate coverage-padding tests inside ropus).
/// 4. Everything else falls back to "Other" so unexpected binaries
///    (`harness::`, future integration crates, lone-fn FQNs) surface as an
///    audit trail instead of polluting Types.
fn categorise_fqn(fqn: &str, binary: &str) -> &'static str {
    if binary == "conformance"
        || CONFORMANCE_BINARIES.contains(&binary)
        || fqn.contains("conformance::")
    {
        return "Conformance";
    }
    let head = fqn.split("::").next().unwrap_or(fqn);
    match head {
        "silk" => "SILK",
        "celt" => "CELT",
        "opus" => "Opus",
        "dnn" => "DNN",
        "types" => "Types",
        "coverage_tests" => "Coverage",
        _ => "Other",
    }
}

fn render_phase_summary(out: &mut String, ctx: &ReportContext<'_>) {
    out.push_str("<h2>Phase summary</h2>\n");
    out.push_str("<table>\n<tr><th>Phase</th><th class=\"num\">Total</th><th class=\"num\">Passed</th><th class=\"num\">Failed</th><th class=\"num\">Skipped</th><th class=\"num\">Duration</th></tr>\n");

    // Unit Tests
    let t = &ctx.tests.tests;
    let total = t.total_passed + t.total_failed + t.total_ignored;
    let fail_cls = if t.total_failed > 0 || t.build_failed {
        " fail"
    } else {
        ""
    };
    let tests_dur = if t.skipped {
        "skipped".to_string()
    } else {
        format_duration(t.duration_ms)
    };
    out.push_str(&format!(
        "<tr><td>Unit Tests</td><td class=\"num\">{total}</td><td class=\"num\">{passed}</td><td class=\"num{fail_cls}\">{failed}</td><td class=\"num\">{ignored}</td><td class=\"num\">{dur}</td></tr>\n",
        passed = t.total_passed,
        failed = if t.build_failed { "build-failed".to_string() } else { t.total_failed.to_string() },
        ignored = t.total_ignored,
        dur = html_escape(&tests_dur),
    ));

    // Quality
    let q = ctx.quality;
    let (q_total, q_passed, q_failed, q_dur_ms) = if q.skipped {
        (0u32, 0u32, 0u32, 0u64)
    } else {
        let total = q.checks.len() as u32;
        let passed = q.checks.iter().filter(|c| c.passed).count() as u32;
        let failed = total - passed;
        let dur_ms: u64 = q.checks.iter().map(|c| c.duration.as_millis() as u64).sum();
        (total, passed, failed, dur_ms)
    };
    let q_fail_cls = if q_failed > 0 { " fail" } else { "" };
    let q_dur_str = if q.skipped {
        "skipped".to_string()
    } else {
        format_duration(q_dur_ms)
    };
    out.push_str(&format!(
        "<tr><td>Quality</td><td class=\"num\">{q_total}</td><td class=\"num\">{q_passed}</td><td class=\"num{q_fail_cls}\">{q_failed}</td><td class=\"num\">{skipped}</td><td class=\"num\">{dur}</td></tr>\n",
        skipped = if q.skipped { 1 } else { 0 },
        dur = html_escape(&q_dur_str),
    ));

    // Ambisonics
    let a = ctx.ambisonics;
    let (a_total, a_passed, a_failed) = if a.skipped {
        (0u32, 0u32, 0u32)
    } else {
        let total = a.per_order.len() as u32;
        let passed = a.per_order.iter().filter(|o| o.passed).count() as u32;
        (total, passed, total - passed)
    };
    let a_failed_disp = if a.build_failed {
        "build-failed".to_string()
    } else {
        a_failed.to_string()
    };
    let a_fail_cls = if a_failed > 0 || a.build_failed {
        " fail"
    } else {
        ""
    };
    let a_dur_str = if a.skipped {
        "skipped".to_string()
    } else {
        format_duration(a.duration_ms)
    };
    out.push_str(&format!(
        "<tr><td>Ambisonics</td><td class=\"num\">{a_total}</td><td class=\"num\">{a_passed}</td><td class=\"num{a_fail_cls}\">{failed}</td><td class=\"num\">{skipped}</td><td class=\"num\">{dur}</td></tr>\n",
        failed = html_escape(&a_failed_disp),
        skipped = if a.skipped { 1 } else { 0 },
        dur = html_escape(&a_dur_str),
    ));

    // Bench (WARN-only stage; failed count is crashed-vector count)
    let b = ctx.bench;
    let (b_total, b_passed, b_crashed, b_skipped_rows) = if b.skipped {
        (0u32, 0u32, 0u32, 0u32)
    } else {
        let total = b.vectors.len() as u32;
        let crashed = b.vectors.iter().filter(|v| v.crashed).count() as u32;
        let skipped_rows = b.vectors.iter().filter(|v| v.skipped).count() as u32;
        let passed = total - crashed - skipped_rows;
        (total, passed, crashed, skipped_rows)
    };
    let b_dur_str = if b.skipped {
        "skipped".to_string()
    } else {
        format_duration(b.duration_ms)
    };
    let b_failed_cls = if b.build_failed || b_crashed > 0 {
        " warn"
    } else {
        ""
    };
    let b_failed_disp = if b.build_failed {
        "build-failed".to_string()
    } else {
        b_crashed.to_string()
    };
    out.push_str(&format!(
        "<tr><td>Bench</td><td class=\"num\">{b_total}</td><td class=\"num\">{b_passed}</td><td class=\"num{b_failed_cls}\">{failed}</td><td class=\"num\">{skipped}</td><td class=\"num\">{dur}</td></tr>\n",
        failed = html_escape(&b_failed_disp),
        skipped = if b.skipped { 1 } else { b_skipped_rows },
        dur = html_escape(&b_dur_str),
    ));

    // Fuzz sanity (policy-owned stage; default full-test leaves it not requested)
    let f = ctx.fuzz;
    let f_total = f.targets.len() as u32;
    let f_failed = if f.status == FuzzStatus::Fail { 1 } else { 0 };
    let f_passed = if f.status == FuzzStatus::Pass || f.status == FuzzStatus::Warn {
        f_total.saturating_sub(f_failed)
    } else {
        0
    };
    let f_skipped = if f.status == FuzzStatus::NotRequested {
        1
    } else {
        0
    };
    let f_fail_cls = match f.status {
        FuzzStatus::Fail => " fail",
        FuzzStatus::Warn => " warn",
        _ => "",
    };
    let f_failed_disp = match f.status {
        FuzzStatus::Fail => "failed".to_string(),
        FuzzStatus::Warn => "warn".to_string(),
        _ => f_failed.to_string(),
    };
    let f_dur = if f.status == FuzzStatus::NotRequested {
        "not requested".to_string()
    } else {
        format_duration(f.duration_ms)
    };
    out.push_str(&format!(
        "<tr><td>Fuzz sanity</td><td class=\"num\">{f_total}</td><td class=\"num\">{f_passed}</td><td class=\"num{f_fail_cls}\">{failed}</td><td class=\"num\">{f_skipped}</td><td class=\"num\">{dur}</td></tr>\n",
        failed = html_escape(&f_failed_disp),
        dur = html_escape(&f_dur),
    ));

    out.push_str("</table>\n");
}

fn render_fuzz_sanity(out: &mut String, fuzz: &FuzzOutcome) {
    out.push_str("<h2>Fuzz sanity</h2>\n");
    out.push_str(&format!(
        "<p class=\"meta\">Mode: <code>{}</code> &bull; Status: <span class=\"{}\">{}</span></p>\n",
        html_escape(fuzz.mode.as_str()),
        fuzz_status_class(fuzz.status),
        html_escape(fuzz.status.as_str()),
    ));
    if !fuzz.command.is_empty() {
        out.push_str(&format!(
            "<p class=\"meta\">Command: <code>{}</code></p>\n",
            html_escape(&fuzz.command.join(" ")),
        ));
    }
    if !fuzz.issues.is_empty() {
        out.push_str("<ul class=\"failed-tests\">\n");
        for issue in &fuzz.issues {
            out.push_str(&format!("  <li>{}</li>\n", html_escape(issue)));
        }
        out.push_str("</ul>\n");
    }
    if fuzz.targets.is_empty() {
        out.push_str("<p class=\"empty\">(no fuzz target rows)</p>\n");
        return;
    }
    out.push_str("<table>\n<tr><th>Target</th><th>Build</th><th class=\"num\">Crashes</th><th>Replay</th></tr>\n");
    for target in &fuzz.targets {
        let cls = if target.build == "fail" || target.replay == "fail" {
            "fail"
        } else if target.build == "not_checked" || target.replay == "not_checked" {
            "warn"
        } else {
            "pass"
        };
        out.push_str(&format!(
            "<tr><td><code>{}</code></td><td class=\"{}\">{}</td><td class=\"num\">{}</td><td class=\"{}\">{}</td></tr>\n",
            html_escape(&target.name),
            cls,
            html_escape(&target.build),
            target.crashes,
            cls,
            html_escape(&target.replay),
        ));
    }
    out.push_str("</table>\n");
}

