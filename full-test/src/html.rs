    #[test]
    fn module_key_strips_duplicate_binary_prefix() {
        // When the FQN already leads with the binary name — e.g. cargo
        // labels a conformance binary `conformance` and the FQN reads
        // `conformance::decode::test_zeta` — the rendered key would
        // otherwise double up as `conformance::conformance::decode`.
        assert_eq!(
            module_key("conformance::decode::test_zeta", "conformance"),
            "conformance::decode"
        );
        // No strip when the prefix doesn't match.
        assert_eq!(
            module_key("conformance::decode::test_zeta", "ropus"),
            "ropus::conformance::decode"
        );
        // Flat FQN whose only segment equals the binary — no strip; the
        // fallback preserves the binary as its own key.
        assert_eq!(
            module_key("conformance", "conformance"),
            "conformance::conformance"
        );
    }

