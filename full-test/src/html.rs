    #[test]
    fn meta_bench_ratios_roundtrip_via_serde_json() {
        // The `<meta name="bench_ratios">` tag is how a future run can read
        // the previous ratios for regression comparison. We serialise as
        // JSON and assert a serialize → parse → equal round-trip so the
        // regression reader (Phase 5 follow-up) is a one-liner.
        let vectors = vec![
            VectorBench {
                label: "A".to_string(),
                bitrate: 64_000,
                skipped: false,
                skip_reason: None,
                crashed: false,
                crash_reason: None,
                c_encode_ms: Some(100.0),
                rust_encode_ms: Some(85.0),
                enc_ratio: Some(0.85),
                c_decode_ms: Some(20.0),
                rust_decode_ms: Some(22.0),
                dec_ratio: Some(1.1),
            },
            VectorBench {
                // Delimiter characters that would have collided with the
                // old `|`/`;` DSL; JSON preserves them verbatim.
                label: "B;|escape".to_string(),
                bitrate: 128_000,
                skipped: true,
                skip_reason: Some("fixture missing".to_string()),
                crashed: false,
                crash_reason: None,
                c_encode_ms: None,
                rust_encode_ms: None,
                enc_ratio: None,
                c_decode_ms: None,
                rust_decode_ms: None,
                dec_ratio: None,
            },
        ];
        let meta = bench_ratios_meta(&vectors);
        let parsed: Vec<BenchRatio> = serde_json::from_str(&meta).expect("round-trip parse");
        assert_eq!(parsed.len(), 2);
        assert_eq!(parsed[0].label, "A");
        assert_eq!(parsed[0].enc, Some(0.85));
        assert_eq!(parsed[0].dec, Some(1.1));
        assert_eq!(parsed[1].label, "B;|escape");
        assert_eq!(parsed[1].enc, None);
        assert_eq!(parsed[1].dec, None);
    }

