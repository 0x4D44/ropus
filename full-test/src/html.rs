//! Stage 5 — self-contained HTML report.
//!
//! Dark-theme, inline CSS, no external assets. Built by direct string
//! concatenation — no templating engine, so the dependency surface stays
//! tiny and every variable interpolation is explicit (and therefore easy
//! to route through `html_escape`).
//!
//! Sections mirror HLD § Output format:
//!
//! 1. Header (banner badge + timestamp/version/branch/commit metadata).
//! 2. Category summary (CELT / SILK / Opus / DNN / Types / Coverage /
//!    Conformance).
//! 3. Phase summary (Unit Tests / Quality / Ambisonics / Bench).
//! 4. Module breakdown (`<crate>::<module>`).
//! 5. Failed tests (fully-qualified names).
//! 6. Coverage metrics (lines / functions / branches / regions).
//! 7. Benchmark table (Vector / Encode C / Encode Rust / Enc Ratio /
//!    Decode C / Decode Rust / Dec Ratio).
//!
//! XSS hygiene: every variable string is routed through `html_escape`, even
//! ones we think we control (commit SHA, branch name, version, test FQNs).

