fn render_module_breakdown(out: &mut String, per_test: &[TestOutcome]) {
    out.push_str("<h2>Module breakdown</h2>\n");
    if per_test.is_empty() {
        out.push_str("<p class=\"empty\">No module breakdown available.</p>\n");
        return;
    }
    // Group by `<binary>::<module>` where module is the first two FQN
    // components that aren't `tests`. `silk::decoder::tests::test_foo` →
    // `ropus::silk::decoder`. For a lone `integration_foo::bar::test` we
    // fall back to `<binary>::<first_component>`.
    let mut modules: BTreeMap<String, (u32, u32, u32)> = BTreeMap::new();
    for t in per_test {
        let key = module_key(&t.fqn, &t.binary);
        let slot = modules.entry(key).or_insert((0, 0, 0));
        slot.0 += 1;
        match t.outcome {
            TestOutcomeKind::Pass => slot.1 += 1,
            TestOutcomeKind::Fail => slot.2 += 1,
            TestOutcomeKind::Ignored => {}
        }
    }
    out.push_str("<table>\n<tr><th>Module</th><th class=\"num\">Total</th><th class=\"num\">Passed</th><th class=\"num\">Failed</th></tr>\n");
    for (module, (total, passed, failed)) in &modules {
        let fail_cls = if *failed > 0 { " fail" } else { "" };
        out.push_str(&format!(
            "<tr><td>{}</td><td class=\"num\">{}</td><td class=\"num\">{}</td><td class=\"num{}\">{}</td></tr>\n",
            html_escape(module),
            total,
            passed,
            fail_cls,
            failed,
        ));
    }
    out.push_str("</table>\n");
}

/// Derive a `<crate>::<module>` key from an FQN + binary label. The FQN tail
/// (`::tests::test_foo`) is dropped so a single module with many tests rolls
/// up to one row. The binary prefix is preserved so tests under `ropus` and
/// `conformance` stay distinct even when the module path coincides. If the
/// FQN already leads with the binary name (e.g. `conformance::decode::…`
/// inside binary `conformance`), the duplicate prefix is dropped so the
/// rendered key doesn't double up.
fn module_key(fqn: &str, binary: &str) -> String {
    // Strip a leading `<binary>::` from the FQN so we don't render
    // `conformance::conformance::decode`. Skip the strip when it would
    // leave the FQN empty (i.e. the FQN *is* the binary name).
    let dup_prefix = format!("{binary}::");
    let fqn = fqn.strip_prefix(&dup_prefix).unwrap_or(fqn);
    let parts: Vec<&str> = fqn.split("::").collect();
    // Strip a trailing `tests` segment + the test function name. Examples:
    //   silk::decoder::tests::test_foo → silk::decoder
    //   celt::bands::test_beta         → celt::bands
    //   lone_fn                        → (fallback: use fqn unchanged)
    let module_parts: Vec<&str> = match parts.len() {
        0 | 1 => return format!("{binary}::{fqn}"),
        _ => {
            // Drop the final function-name segment, and an optional
            // penultimate `tests` segment.
            let mut end = parts.len() - 1;
            if end > 0 && parts[end - 1] == "tests" {
                end -= 1;
            }
            parts[..end].to_vec()
        }
    };
    if module_parts.is_empty() {
        format!("{binary}::{fqn}")
    } else {
        format!("{binary}::{}", module_parts.join("::"))
    }
}

