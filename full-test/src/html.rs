fn render_coverage_row(out: &mut String, scope: &str, m: &CoverageMetrics) {
    out.push_str(&format!(
        "<tr><td>{}</td><td class=\"num\">{}</td><td class=\"num\">{}</td><td class=\"num\">{}</td><td class=\"num\">{}</td></tr>\n",
        html_escape(scope),
        html_escape(&cell_cov(m.lines_covered, m.lines_total)),
        html_escape(&cell_cov(m.functions_covered, m.functions_total)),
        html_escape(&cell_cov(m.branches_covered, m.branches_total)),
        html_escape(&cell_cov(m.regions_covered, m.regions_total)),
    ));
}

fn cell_cov(covered: u64, total: u64) -> String {
    if total == 0 {
        return "0 / 0 (n/a)".to_string();
    }
    let pct = (covered as f64) * 100.0 / (total as f64);
    format!("{covered} / {total} ({pct:.1}%)")
}

fn render_benchmarks(out: &mut String, vectors: &[VectorBench]) {
    out.push_str("<h2>Benchmark table</h2>\n");
    if vectors.is_empty() {
        out.push_str("<p class=\"empty\">(benchmarks skipped)</p>\n");
        return;
    }
    out.push_str("<table>\n<tr><th>Vector</th><th>Encode C</th><th>Encode Rust</th><th class=\"num\">Enc Ratio</th><th>Decode C</th><th>Decode Rust</th><th class=\"num\">Dec Ratio</th></tr>\n");
    for v in vectors {
        if v.skipped {
            out.push_str(&format!(
                "<tr><td>{}</td><td colspan=\"6\" class=\"empty\">{}</td></tr>\n",
                html_escape(&v.label),
                html_escape(v.skip_reason.as_deref().unwrap_or("missing fixture")),
            ));
            continue;
        }
        if v.crashed {
            out.push_str(&format!(
                "<tr><td>{}</td><td colspan=\"6\" class=\"warn\"><em>crashed: {}</em></td></tr>\n",
                html_escape(&v.label),
                html_escape(v.crash_reason.as_deref().unwrap_or("(no reason captured)")),
            ));
            continue;
        }
        out.push_str(&format!(
            "<tr><td>{}</td><td class=\"num\">{}</td><td class=\"num\">{}</td><td class=\"num{}\">{}</td><td class=\"num\">{}</td><td class=\"num\">{}</td><td class=\"num{}\">{}</td></tr>\n",
            html_escape(&v.label),
            html_escape(&cell_ms(v.c_encode_ms)),
            html_escape(&cell_ms(v.rust_encode_ms)),
            ratio_class(v.enc_ratio),
            html_escape(&cell_ratio(v.enc_ratio)),
            html_escape(&cell_ms(v.c_decode_ms)),
            html_escape(&cell_ms(v.rust_decode_ms)),
            ratio_class(v.dec_ratio),
            html_escape(&cell_ratio(v.dec_ratio)),
        ));
    }
    out.push_str("</table>\n");
}

fn cell_ms(v: Option<f64>) -> String {
    match v {
        Some(x) if x.is_finite() => format!("{x:.2} ms"),
        _ => "—".to_string(),
    }
}

fn cell_ratio(v: Option<f64>) -> String {
    match v {
        Some(x) if x.is_finite() => format!("{x:.3}x"),
        _ => "—".to_string(),
    }
}

/// Extra CSS class to append after `num` in a ratio cell. Amber above the
/// WARN threshold, green below 1.0x (Rust faster than C), no extra class
/// otherwise. Returned as a leading-space suffix so the caller can splice it
/// directly into `class="num{...}"` — emitting a second `class=` attribute
/// is invalid HTML and some browsers silently drop the later one.
fn ratio_class(r: Option<f64>) -> &'static str {
    match r {
        Some(x) if x.is_finite() && x > BENCH_WARN_RATIO => " ratio-warn",
        Some(x) if x.is_finite() && x < 1.0 => " ratio-fast",
        _ => "",
    }
}

