/// All the inputs the renderer needs, bundled. Lifetime-borrowed from
/// `main.rs`'s stage outcomes — no heavy clones.
pub struct ReportContext<'a> {
    pub commit_sha: &'a str,
    pub branch: &'a str,
    pub version: &'a str,
    pub commit_subject: &'a str,
    pub timestamp: chrono::DateTime<chrono::Local>,
    pub banner: Banner,
    pub ietf_vectors: IetfVectorProvision,
    pub ietf_vectors_present: bool,
    pub preflight: PreflightOutcome,
    pub options: &'a Options,
    pub quality: &'a QualityOutcome,
    pub tests: &'a TestsOutcome,
    pub fuzz: &'a FuzzOutcome,
    pub ambisonics: &'a AmbisonicsResult,
    pub bench: &'a BenchResult,
}

/// Render the report into a self-contained HTML string. Pure — no I/O;
/// the caller writes the string to disk.
pub fn render(ctx: &ReportContext<'_>) -> String {
    let mut out = String::with_capacity(64 * 1024);

    out.push_str("<!DOCTYPE html>\n<html lang=\"en\">\n<head>\n");
    out.push_str("<meta charset=\"utf-8\">\n");
    out.push_str("<meta name=\"viewport\" content=\"width=device-width, initial-scale=1\">\n");
    out.push_str("<title>ropus Test Report</title>\n");
    // JSON array of `{label, enc, dec}` — parseable back with
    // `serde_json::from_str::<Vec<BenchRatio>>` so Phase 5's regression-vs-
    // previous-report logic is a one-liner. Embedded in a single-quoted
    // attribute so the JSON's double quotes pass through unescaped;
    // `html_escape` still runs to neutralise apostrophes and angle brackets
    // that could theoretically appear in a label.
    out.push_str(&format!(
        "<meta name=\"bench_ratios\" content='{}'>\n",
        html_escape(&bench_ratios_meta(&ctx.bench.vectors)),
    ));
    out.push_str("<style>\n");
    out.push_str(CSS);
    out.push_str("</style>\n</head>\n<body>\n<div class=\"container\">\n");

    render_header(&mut out, ctx);
    render_category_summary(&mut out, &ctx.tests.tests.per_test);
    render_phase_summary(&mut out, ctx);
    render_release_preflight(&mut out, &ctx.preflight);
    render_fuzz_sanity(&mut out, ctx.fuzz);
    render_module_breakdown(&mut out, &ctx.tests.tests.per_test);
    render_failed_tests(&mut out, &ctx.tests.tests.failed_test_names);
    render_coverage(&mut out, ctx);
    render_benchmarks(&mut out, &ctx.bench.vectors);

    out.push_str("<footer>Generated by <code>full-test</code> — ");
    out.push_str(env!("CARGO_PKG_NAME"));
    out.push(' ');
    out.push_str(env!("CARGO_PKG_VERSION"));
    out.push_str("</footer>\n");

    out.push_str("</div>\n</body>\n</html>\n");
    out
}

